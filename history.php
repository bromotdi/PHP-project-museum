                <h1>Історія Музею Ханенків</h1>
                <p>Історія Музею Ханенків сягає 1870-х років, коли Богдан Ханенко (1849-1917) узяв шлюб із Варварою Терещенко (1852-1922).  Молоде подружжя захопилося ідеєю власного художнього зібрання.</p>  

                <p>Ханенки багато подорожували Європою, відвідували аукціони і приватні колекції. Керувалися порадами провідних знавців історії мистецтва. У результаті за 40 років пошуку Ханенки зібрали надзвичайно цінну колекцію мистецтва.</p>
               
                <p>Відкрити у Києві музей світового мистецтва - головна справа життя родини Ханенків. Про це свідчить заповіт Богдана Ханенка від 1917 року. У 1918 році Варвара Ханенко підписує «Дарчу заяву», якою передає колекцію, будинок і книгозбірню у власність Української Академії Наук.</p>
               
                <p>Утім, історія музею складається інакше. У червні 1919 року більшовицька влада Декретом Раднаркому УСРР націоналізує майно родини Ханенків. У домі Ханенків відкрито державний музей мистецтва. Першим упорядником музею стає мистецтвознавець та художник Георгій Лукомський (1884-1952). Разом із ним активну участь в створенні перших експозицій бере Варвара Ханенко. Завдяки підтримці української інтелігенції літня Варвара Ханенко добивається права і надалі мешкати при музеї. До кінця життя вона очолює Комітет музею.</p>
               
                <p>Утім 1924 року, за два роки після смерті Варвари Ханенко, імена Ханенків прибрано з назви музею «за відсутності у Ханенка революційних заслуг перед пролетарською культурою»….</p>
               
                <p>Роки Другої світової війни найцінніша частина музейних творів перебула в евакуації в м.Уфа (Башкоторстан). Ту частину, що залишилася в Києві, була пограбовано нацистами при відступі 1943 року. Сьогодні музей веде роботу з пошуку втраченої частини колекції</p>
               
                <p>З приходом до музею нового менеджменту на чолі з директоркою Вірою Виноградовою  в історії музею розпочинається епоха відновлення історичної пам’яті та інтенсивного розвитку. 1998 року у відреставрованому будинку Ханенків відкрито оновлені експозиції мистецтва Європи 14-19 століть. 1999 року імена засновників повернено до офіційної назви музею «Музей мистецтв імені Богдана та Варвари Ханенків». 2004 року в домі Ханенків відкрито постійну залу «сінайських» ікон 6-7 століть. У 2006 році в сусідньому будинку розпочала роботу перша в історії музею велика постійна експозиція мистецтва Азії.</p>
               
                <p>Перші десятиліття 21 століття – час активізації наукових досліджень колекції та історії музею. Формується нова філософія та практика освітньої роботі та музейних сервісів. Проведено перше масштабне дослідження з вивчення відвідувачів. Величезну увагу музей приділяє родинам, дітям та молоді. Розвивається інклюзивний напрямок: програми та послуги для людей з інвалідністю, програми для літніх людей, для малозабезпечених та безхатьків, батьків у декреті.  </p>
